# Запрашиваем у пользователя ввод чисел, разделенных пробелами
numbers_input = input("Введите числа через пробел: ")

# Преобразуем введенную строку в список чисел
# Разделяем строку по пробелам и преобразуем каждый элемент в целое число
numbers = [int(x) for x in numbers_input.split()]

# Создаем новый список, содержащий квадраты чисел из исходного списка
# Используем генератор списка для преобразования каждого элемента
squares = [x ** 2 for x in numbers]

# Выводим исходный список чисел
print("Исходный список:", numbers)

# Выводим список с квадратами чисел
print("Квадраты чисел:", squares)
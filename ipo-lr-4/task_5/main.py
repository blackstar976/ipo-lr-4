# Запрашиваем у пользователя ввод чисел, разделенных пробелами
user_input = input("Введите числа через пробел: ")

# Разделяем введенную строку на отдельные элементы по пробелам
# Получаем список строк, каждая из которых представляет одно число
numbers_as_strings = user_input.split()

# Преобразуем каждый элемент списка из строки в целое число
# Создаем новый список numbers, содержащий числовые значения
numbers = [int(num) for num in numbers_as_strings]

# Инициализируем переменную для хранения суммы
# Начальное значение суммы равно 0
total_sum = 0

# Используем цикл for для перебора всех чисел в списке
# Переменная number поочередно принимает значение каждого элемента списка
for number in numbers:
    # Добавляем текущее число к общей сумме
    total_sum = total_sum + number
    # Эквивалентная запись: total_sum += number

# Выводим исходный список чисел для наглядности
print("Введенные числа:", numbers)

# Выводим вычисленную сумму всех чисел
print("Сумма всех чисел:", total_sum)
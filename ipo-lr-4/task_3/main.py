# Запрашиваем у пользователя число и преобразуем ввод в целое число
num = int(input("Введите число для проверки: "))

# Простое число должно быть больше 1
if num > 1:
    # Начинаем проверку с делителя 2
    divisor = 2
    # Флаг для отслеживания, является ли число простым
    is_prime = True
    
    # Проверяем делители от 2 до квадратного корня из числа (включительно)
    # Если число делится нацело на любой делитель в этом диапазоне - оно не простое
    while divisor * divisor <= num:
        # Проверяем, делится ли число на текущий делитель без остатка
        if num % divisor == 0:
            # Если делится, число не является простым
            is_prime = False
            # Прерываем цикл, так как уже нашли делитель
            break
        # Переходим к следующему делителю
        divisor += 1
    
    # Проверяем результат проверки
    if is_prime:
        # Выводим сообщение, что число простое
        print(f"{num} - простое число")
    else:
        # Выводим сообщение, что число составное
        print(f"{num} - составное число")
else:
    # Числа 0, 1 и отрицательные не являются простыми
    print(f"{num} - не является простым числом")
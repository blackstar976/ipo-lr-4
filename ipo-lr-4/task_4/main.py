# Запрашиваем у пользователя верхнюю границу диапазона
n = int(input("Введите число n: "))

# Создаем генератор списка, который:
# - перебирает все числа от 1 до n включительно
# - для каждого числа проверяет условие делимости на 5
# - добавляет число в список только если оно делится на 5 без остатка
numbers_divisible_by_5 = [x for x in range(1, n + 1) if x % 5 == 0]

# Выводим результат
print(f"Числа от 1 до {n}, которые делятся на 5:")
print(numbers_divisible_by_5)
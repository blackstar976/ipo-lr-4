# Запрашиваем у пользователя целое число и преобразуем ввод в целочисленный тип
n = int(input("Введите число: "))

# Выводим сообщение о том, какие числа будут показаны
print(f"Четные числа от 0 до {n}:")

# Цикл for для перебора всех чисел от 0 до введенного числа (включительно)
for i in range(0, n + 1):
    # Проверяем, является ли текущее число четным
    if i % 2 == 0:
        # Если число четное, выводим его
        print(i)